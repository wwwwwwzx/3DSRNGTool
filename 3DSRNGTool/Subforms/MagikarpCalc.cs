        public static string calc(string inlist)
        {
            uint[] list = new uint[4];
            for (int k = 0; k < 4; k++)
            {
                uint t = 1;
                for (int i = 0; i < 32; i++)
                {
                    if (i + k * 32 == 127) break;
                    if (inlist[i + k * 32] == '1')
                        list[k] ^= t;
                    t <<= 1;
                }
            }
            uint[] result = mul(list);
            return string.Join(",", result.Select(v => v.ToString("X8")).Reverse());
        }

        private static uint[] mul(uint[] vec)
        {
            uint[] tarr = new uint[4];
            uint[] result = new uint[4];
            uint t = 1, c = 0;
            for (int i = 0; i < 127; i++)
            {
                if (i == 31 || i == 63 || i == 95)
                {
                    t = 1;
                    c++;
                }
                for (int k = 0; k < 4; k++)
                {
                    tarr[k] = invlist[i * 4 + k] & vec[k];
                    while (tarr[k] > 0)
                    {
                        if ((tarr[k] & 1) == 1)
                            result[c] ^= t;
                        tarr[k] >>= 1;
                    }
                }
                t <<= 1;
            }
            return result;
        }

